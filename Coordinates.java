public class Coordinates
{
    private double coordinateX;
    private double coordinateY;

    public Coordinates(double x, double y)
    {
        coordinateX = x;
        coordinateY = y;
    }

    public double getX() {return coordinateX;}
    public double getY() {return coordinateY;}

}
