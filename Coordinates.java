public class Coordinates
{
    private int coordinateX;
    private int coordinateY;

    public Coordinates(int x, int y)
    {
        coordinateX = x;
        coordinateY = y;
    }

    public int getX() {return coordinateX;}
    public int getY() {return coordinateY;}

}
